/**
 * Converts a transport-layer `INV:` memo string into on-chain message bytes.
 *
 * This helper is used by `PaymentsModule.send()` (and split executor paths)
 * to encode invoice references from the Nostr memo into the structured
 * `TransferTransactionData.message` field. Non-invoice memos return `null`
 * so that private user text is never written to the permanent on-chain record.
 *
 * Steps:
 * 1. If `memo` is absent or does not match the `INV:` format → return `null`.
 * 2. Build a {@link TransferMessagePayload} from the parsed reference:
 *    - `inv.id` = normalized `invoiceId`
 *    - `inv.dir` = direction code mapped from `paymentDirection`
 *    - `inv.ra` = `refundAddress` if provided
 *    - `inv.ct` = `{ a: contact.address, u?: contact.url }` if provided
 *    - `txt` = `freeText` if present
 * 3. Encode and return the payload bytes.
 *
 * @param memo - Transport memo string (e.g. `INV:abc...:F hello`). May be
 *   `undefined` for transfers without a memo.
 * @param refundAddress - Optional DIRECT:// address for auto-return routing.
 *   Embedded in `inv.ra` on-chain; NOT included in the transport memo (privacy).
 * @param contact - Optional contact info for the payer. Embedded in `inv.ct`
 *   on-chain; NOT included in the transport memo (privacy).
 * @returns UTF-8 JSON bytes for the on-chain message, or `null` if the memo
 *   is absent or non-invoice.
 *
 * @see ACCOUNTING-SPEC.md §4.7
 */
export function parseInvoiceMemoForOnChain(
  memo: string | undefined,
  refundAddress?: string,
  contact?: { address: string; url?: string },
): Uint8Array | null {
  if (!memo) return null;

  const ref = parseInvoiceMemo(memo);
  if (!ref) {
    // Non-invoice memo: keep transport-only to preserve privacy
    return null;
  }

  // Validate refundAddress before embedding on-chain (same rules as decodeTransferMessage)
  const validRefundAddress =
    refundAddress &&
    typeof refundAddress === 'string' &&
    refundAddress.startsWith('DIRECT://') &&
    refundAddress.length > 9 &&
    refundAddress.length <= 256
      ? refundAddress
      : undefined;

  // Validate contact fields before embedding on-chain (same rules as decodeTransferMessage)
  let validContact: { a: string; u?: string } | undefined;
  if (contact) {
    const validAddr =
      typeof contact.address === 'string' &&
      contact.address.startsWith('DIRECT://') &&
      contact.address.length > 9 &&
      contact.address.length <= 256;

    if (validAddr) {
      validContact = { a: contact.address };
      if (
        contact.url &&
        typeof contact.url === 'string' &&
        (contact.url.startsWith('https://') || contact.url.startsWith('wss://')) &&
        contact.url.length <= 2048 &&
        // eslint-disable-next-line no-control-regex -- intentional: reject control chars in URL
        !/[\x00-\x1f]/.test(contact.url)
      ) {
        validContact.u = contact.url;
      }
    }
  }

  const inv: NonNullable<TransferMessagePayload['inv']> = {
    id: ref.invoiceId,
    dir: DIRECTION_TO_CODE[ref.paymentDirection],
    ...(validRefundAddress ? { ra: validRefundAddress } : {}),
    ...(validContact ? { ct: validContact } : {}),
  };

  const payload: TransferMessagePayload = {
    inv,
    ...(ref.freeText ? { txt: ref.freeText } : {}),
  };

  return encodeTransferMessage(payload);
}
